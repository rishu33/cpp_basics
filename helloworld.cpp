#include <iostream>

int main(){

    // This is a single-line comment.
    
    std::cout << "I like pizza!";                // Standard character output
    std::cout << std::endl;                      // End of line
    std::cout << "It's really good!" << '\n';    // '\n' is another way to line-break
    std::cout << "Do you like pizza?";
    
    /*
    This
    is
    a
    multi-line
    comment.
    */
    
    return 0;
}